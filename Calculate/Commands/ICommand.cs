/*
 * ICommand.cs - Интерфейс команды (Command Pattern)
 * 
 * Проблема, которую решает:
 * - Без Command: прямые вызовы методов логики, нет поддержки отмены
 * - С Command: операции инкапсулированы в объекты, легко добавлять отмену
 * 
 * Паттерн: Command (Команда)
 * - Инкапсулирует запрос как объект
 * - Позволяет параметризовать объекты выполняемыми операциями
 * - Поддерживает отмену операций и историю действий
 * 
 * Преимущества:
 * - Отделение отправителя команды от её получателя
 * - Легкая реализация отмены/повтора операций
 * - Упрощение расширения функциональности
 */

namespace Calculator.Commands
{
    public interface ICommand
    {
        void Execute();
        void Undo();
    }
}